// col-12 <768px => hiện 1 cột
// Các lớp cột cho biết số cột bạn muốn sử dụng trong số 12 cột có thể có trên mỗi hàng.
// Vì vậy, nếu bạn muốn có ba cột có chiều rộng bằng nhau, bạn có thể sử dụng .col-4.
// col-md-4 Khi màn hình ≥768px => hiện 3 cột
// forEach thì nó là loop

let currentPage = 1; // Mac dinh bat dau o trang 1
const perPage = 12; // Moi trang se hien thi 12 muc
const totalPage = Math.ceil(data.length / perPage); // Tinh tong so trang can co
perUser = data.slice((currentPage - 1) * perPage, currentPage * perPage); // Lay danh sach muc hien tai
// Trang dau tien
// data.slice(0,12)

const mainContent = document.querySelector('.main-content .row');

// Tai du lieu
function loadData() {
    let contentHtml = ''; // Khoi tao chuoi HTML de chua cac muc
    perUser.forEach((item) => { // Duyet qua moi muc trong perUser
        let packageHtml = `
            <div class="col-12 col-md-4 mb-3">
                <div class="vx_item">
                    <a href="${item.src}">
                        <img src="${item.img}" alt="${item.title}"> 
                        <div class="vaccine_name" title="${item.title}">${item.title}</div> 
                    </a>
                    <div class="vaccine-content">${item.text}</div> 
                </div>
            </div>`;
        contentHtml += packageHtml;
    });
    mainContent.innerHTML = contentHtml; // Gan noi dung da tao vao phan tu mainContent
    listPage(); // Goi ham listPage de hien thi cac so trang
}

// Ham de hien thi cac so trang va cac nut dieu huong (Next, Previous)
function listPage() {
    let listPage = document.querySelector('.listPage');
    listPage.innerHTML = '';

    // Nut Previous (trang truoc)
    if (currentPage != 1) {
        let prev = document.createElement('li');
        prev.innerHTML = '<i class="fa-solid fa-arrow-left"></i>';
        prev.addEventListener('click', () => changePage(currentPage - 1));
        listPage.appendChild(prev);
    }

    // Cac so trang
    for (let i = 1; i <= totalPage; i++) {
        let newPage = document.createElement('li');
        newPage.innerText = i;
        if (i === currentPage) { // Neu la trang hien tai
            newPage.classList.add('activePage'); // Them lop activePage de danh dau trang hien tai
        }
        newPage.addEventListener('click', () => changePage(i));
        listPage.appendChild(newPage); // Them so trang vao danh sach trang
    }

    // Nut Next (trang sau)
    if (currentPage != totalPage) { // Neu khong phai trang cuoi cung
        let next = document.createElement('li'); // Tao phan tu li cho nut Next
        next.innerHTML = '<i class="fa-solid fa-arrow-right"></i>';
        next.addEventListener('click', () => changePage(currentPage + 1));
        listPage.appendChild(next);
    }
}

// Ham de thay doi trang
function changePage(i) {
    currentPage = i; // Cap nhat trang hien tai
    perUser = data.slice((currentPage - 1) * perPage, currentPage * perPage); // Cap nhat du lieu cho trang moi
    loadData(); // Goi lai ham loadData de tai lai du lieu cho trang moi
}

// Goi ham loadData de hien thi du lieu ban dau khi trang tai
loadData();



